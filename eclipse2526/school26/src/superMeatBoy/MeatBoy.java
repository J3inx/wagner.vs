package superMeatBoy;

public class MeatBoy {
	private int xPos = 50, yPos = 300, width = 75, height = 75;
	
	
	public int getxPos() {
		return xPos;
	}


	


	public int getyPos() {
		return yPos;
	}


	


	public int getWidth() {
		return width;
	}


	


	public int getHeight() {
		return height;
	}


	

	public MeatBoy() {
		xPos = 50;
		yPos = 300;
		width = 75;
		height = 75;
	}
	
}
