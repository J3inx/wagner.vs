package Babies;

public class Babies {
		private String name, gender;
		private int count;
		//constructor
		public Babies(String name, String gender, int count) {
			this.name = name;
			this.gender = gender;
					this.count = count;
		}
		public String getName() {
			return name;
		}
		public String getGender() {
			return gender;
		}
		public int getCount() {
			
			return count;
		}
}
